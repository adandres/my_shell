int		my_atoi(const char *str)
{
	int i;
	int nb;
	int neg;

	nb = 0;
	neg = 1;
	i = 0;
	while (str[i] == '\f' || str[i] == '\r' || str[i] == '\t' || str[i] == ' '
			|| str[i] == '\n' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * neg);
}
