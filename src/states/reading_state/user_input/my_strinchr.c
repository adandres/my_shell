#include "my_shell.h"

int	my_strinchr(char *str, int n)
{
	int	nb;
	int	re;
	int	i;

	nb = 0;
	i = 0;
	re = 0;
	while (str[i] && i < n - 1)
	{
		nb++;
		if (str[i] == '\n')
		{
			nb = 0;
			re++;
		}
		i++;
	}
	if (re == 0)
		nb += 3;
	return (nb);
}
